import java.util.*;

public class HashMapBook {
	int id, quantity;
	String name, author, publisher;
	
	public HashMapBook(int id, int quantity, String name, String author, String publisher) {
		super();
		this.id = id;
		this.quantity = quantity;
		this.name = name;
		this.author = author;
		this.publisher = publisher;
	}
}