public class Latihan1 {
	public static void main( String[] args ) {
		int Tono, Ati, Susi, tiangBendera;

		Tono = 20;
		Ati = 4;
		Susi = 8;

		tiangBendera = Tono - Ati;
		tiangBendera = tiangBendera + Susi;

		System.out.println( "Posisi Susi dengan Tiang Bendera adalah " + tiangBendera + " m " );
	}
}