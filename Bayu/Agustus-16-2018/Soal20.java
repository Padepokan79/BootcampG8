public class Soal20 {
	public static void main (String [] args){
		int i, j, k, l;

		for (i = 0; i <= 1; i++) {
			for (j=0; j<= 7; j++) {
			System.out.print("0");	
			}
			System.out.println(" ");
			for (k=0; k<= 7; k++) {
			System.out.print("*");	
			}
			System.out.println(" ");
			for (l=0;l<= 7; l++) {
			System.out.print("=");	
			}
			System.out.println(" ");			
		}
	}
}